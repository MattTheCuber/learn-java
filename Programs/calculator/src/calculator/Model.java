package calculator;

public class Model {
	String systemTotal, outputTotal, sign;

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getSystemTotal() {
		return systemTotal;
	}

	public void setSystemTotal(String systemTotal) {
		this.systemTotal = systemTotal;
	}

	public String getOutputTotal() {
		return outputTotal;
	}

	public void setOutputTotal(String outputTotal) {
		this.outputTotal = outputTotal;
	}

}
